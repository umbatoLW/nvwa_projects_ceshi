/**
 * 逻辑一致性知识库
 * 用途：确保剧本中角色认知、剧情事实的前后一致，避免逻辑矛盾
 */

export const logicConsistencyGuide = `## 逻辑一致性知识库（强制执行）

### 核心原则
剧本的逻辑一致性是观众沉浸感的基础。角色不能对"已知事实"表现出惊讶，剧情事实不能前后矛盾。

---

### 一、角色认知一致性（最重要）

#### 1. 核心规则
**角色认知只能增加，不能减少或矛盾**
- 第N集角色知道某事 → 第N+1集及之后，该角色必须"已经知道"这件事
- 角色不能对"自己之前说过/听过/见过"的事实表现出惊讶或疑惑

#### 2. 认知来源类型
角色可以通过以下方式获得认知：
- **主动说出**：角色在台词中透露自己知道某事（最强证据）
- **被动听到**：角色听到别人说某事
- **亲眼看到**：角色看到某个事实
- **推理得出**：角色根据已知信息推理出结论

#### 3. 典型错误案例
❌ 错误示例：
- 第1集：女主对男友说"我爷爷收养了18个孤儿"（女主已知道）
- 第2集：护士说"你爷爷资助了18个孤儿"，女主惊讶问"什么？"（矛盾！）

✅ 正确处理：
- 第2集：护士说"你爷爷资助了18个孤儿"，女主平静回应"我知道，他告诉过我"
- 或者：第1集女主不说出这个事实，让观众以为她不知道，第2集才揭示

---

### 二、剧情事实一致性

#### 1. 事实类型
- **人物事实**：年龄、身份、关系、经历
- **物品事实**：物品归属、位置、状态
- **事件事实**：发生时间、参与者、结果
- **地点事实**：位置、环境、特征

#### 2. 事实记录规则
每集生成后，必须记录新增的"已确立事实"：
\`\`\`
【第N集确立的事实】
- 人物：林小雨是林建国的孙女
- 关系：男友嫌弃林小雨的出身
- 事件：林建国病危入院
- 物品：军功章（一等功，1953年）
- 认知：林小雨知道爷爷收养18个孤儿（从台词得知）
\`\`\`

#### 3. 事实继承规则
生成第N+1集时，第1~N集的所有事实必须继承，不得矛盾：
- 不得改变已确立的人物关系
- 不得改变已确立的事件结果
- 不得让角色"忘记"已知道的事实

---

### 三、生成时的一致性检查

#### 1. 大纲阶段约束
大纲必须包含每集的"角色认知状态"：
\`\`\`
{
  "episode": 1,
  "title": "捡破烂的爷爷",
  "characterKnowledge": {
    "林小雨": ["爷爷捡破烂为生", "爷爷收养了18个孤儿", "男友嫌弃自己出身"],
    "观众": ["林小雨爷爷是捡破烂的", "爷爷病危"]
  },
  "revealedFacts": ["爷爷收养18孤儿的事实通过林小雨台词揭示"]
}

{
  "episode": 2,
  "title": "军功章的秘密",
  "characterKnowledge": {
    "林小雨": ["爷爷捡破烂为生", "爷爷收养了18个孤儿", "男友嫌弃自己出身", "爷爷有一等功军功章"],
    "观众": ["爷爷是一等功臣", "林小雨之前不知道军功章"]
  },
  "revealedFacts": ["军功章是第2集新揭示的事实，林小雨可以对此惊讶"]
}
\`\`\`

#### 2. 剧本生成时约束
生成每集剧本时，必须传入"角色认知状态"，并严格遵守：
\`\`\`
【第2集生成约束】
角色当前认知状态（第1集结束时）：
- 林小雨已知道：爷爷捡破烂、收养18孤儿、男友嫌弃自己

⚠️ 强制规则：
1. 林小雨可以对"军功章"表示惊讶（这是新事实）
2. 林小雨不能对"爷爷收养18孤儿"表示惊讶（她第1集就知道了）
3. 台词不得出现"什么？爷爷收养了孤儿？"这类矛盾对话
\`\`\`

---

### 四、常见逻辑矛盾类型

| 矛盾类型 | 错误示例 | 正确处理 |
|---------|---------|---------|
| 认知回退 | 第1集说知道，第2集问"什么？" | 保持认知一致，对新事实才能惊讶 |
| 事实篡改 | 第1集说A是B的儿子，第3集说A是B的侄子 | 一旦确立关系，后续不得改变 |
| 时间混乱 | 第1集说3年前，第5集说5年前 | 时间线必须一致 |
| 物品消失 | 第1集给的重要道具，后续完全消失 | 物品要有交代或回收 |
| 人物失踪 | 重要配角突然不再出现 | 要有退场交代 |
| **主体客体混淆** | A救了B，但台词说"我是被救的那个" | 明确动作的执行者和承受者 |

---

### 四点五、主体客体一致性（关键！）

#### 1. 核心规则
**任何动作/事件必须明确"谁做了什么对谁"，不得混淆执行者和承受者**

#### 2. 常见混淆类型

| 混淆类型 | 错误示例 | 正确示例 |
|---------|---------|---------|
| 救人方向混淆 | 司马光救了上官婉儿，但台词说"我是被救的那个" | 司马光说"我救了你" |
| 施受关系颠倒 | A送给B礼物，但说"谢谢你送我礼物" | A说"这是我送给你的" |
| 主动被动颠倒 | A主动提出分手，但说"是你不要我的" | A说"是我提出的分手" |
| 加害受害颠倒 | A打了B，但B说"你为什么伤害我"变成A说 | B说"你为什么打我" |

#### 3. 典型错误案例

❌ **严重错误**：
\`\`\`
场景：司马光砸缸救出上官婉儿
台词：司马光说"救命之恩，当以身相许——但我是被救的那个啊..."

分析：
- 事实：司马光救了上官婉儿（执行者=司马光，承受者=上官婉儿）
- 台词错误：司马光说自己是"被救的那个"（承受者），这与事实相反
- 正确：司马光是救人者，上官婉儿是被救者
\`\`\`

✅ **正确处理**：
\`\`\`
场景：司马光砸缸救出上官婉儿
正确台词：
- 司马光：姑娘，你没事吧？我救你出来了。
- 上官婉儿：你救了我？那你得负责！砸了我的缸，赔我一个相公！

关键点：
- 司马光明确是"救人者"
- 上官婉儿明确是"被救者"
- 后续的"以身相许"玩笑由上官婉儿提出，逻辑通顺
\`\`\`

#### 4. Prompt 强制检查
生成每集剧本时，必须检查：
\`\`\`
for each 动作/事件:
  执行者是谁？（谁做的）
  承受者是谁？（对谁做的）
  台词中是否正确反映了这个关系？
  
如果台词中执行者/承受者颠倒 → 标记为逻辑错误，必须修正
\`\`\`

---

### 五、自动检查规则

生成完成后，必须进行以下检查：

#### 1. 角色认知检查
\`\`\`
for each 角色:
  for each 第N集:
    当前认知 = 第N-1集认知 + 第N集新增认知
    检查：第N集台词中是否出现对"当前认知"的惊讶？
    如果是 → 标记为逻辑矛盾
\`\`\`

#### 2. 事实一致性检查
\`\`\`
for each 事实:
  检查：该事实在后续剧集中是否被改变或矛盾？
  如果是 → 标记为逻辑矛盾
\`\`\`

---

### 六、Prompt模板

#### 大纲生成时添加：
\`\`\`
【逻辑一致性要求】
你生成的大纲必须包含 characterKnowledge 字段，记录每集结束时各角色已知道的事实。

规则：
1. 第N集的 characterKnowledge 必须包含第N-1集的所有认知
2. 新揭示的事实才能让角色表示惊讶
3. 已知事实不能让角色再表示惊讶

示例：
第1集：林小雨说出"爷爷收养了18个孤儿" → 林小雨已知道这个事实
第2集：林小雨不能对"爷爷收养18个孤儿"表示惊讶（她第1集就知道了）
第2集：林小雨可以对"爷爷有一等功军功章"表示惊讶（这是新事实）
\`\`\`

#### 剧本生成时添加：
\`\`\`
【角色认知约束 - 必须遵守】
截至上一集，各角色已知以下事实：
{characterKnowledge}

⚠️ 强制规则：
1. 角色只能对"本集新揭示"的事实表示惊讶
2. 角色对"上一集已知道"的事实不得表现出惊讶或疑惑
3. 检查台词，确保不出现认知矛盾

常见错误：
❌ 角色问"什么？XXX？" 但XXX在上一集已经知道
✅ 角色平静回应"我知道" 或对新事实表示惊讶
\`\`\`
`;

